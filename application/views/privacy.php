<h3>Cookies &amp; Log Files</h3>

 
<p>
Like most web sites, Edge Up Network &amp; Mycity.com uses cookies and web log files to track site usage. A cookie is a tiny data file which resides on your computer which allows Edge Up Network &amp; Mycity.com to recognize you as a user when you return to our site using the same computer and web browser. Unfortunately, if your browser settings do not allow cookies, you will not be able to use our website. Like the information you enter at registration or in your Profile, cookie and log file data is used to customize your experience on the web site.</p>
<p>
One type of cookie, known as a “persistent” cookie, is set once you've logged in to your Edge Up Network &amp; Mycity.com account. The next time you visit the Edge Up Network &amp; Mycity.com web site, the persistent cookie will allow us to recognize you as an existing user so you will not need to login before using the service. In order to access or change any of your private information (like a credit card number), however, you must log in securely again with your password even if your computer contains your persistent cookie. Additionally, if you log out of the Edge Up Network &amp; Mycity.com site you will need to re-enter your login credentials the next time you visit in order to log in to your account. Another type of cookie, called a “session” cookie, is used to identify a particular visit to the Edge Up Network &amp; Mycity.com sites. Session cookies expire after a short time or when you close your web browser.</p>
<p>
Due to the communications standards on the Internet, when you visit the Edge Up Network &amp; Mycity.com web site we automatically receive the URL of the site from which you came and the site to which you are going when you leave Edge Up Network &amp; Mycity.com. We also receive the Internet protocol (IP) address of your computer (or the proxy server you use to access the World Wide Web), your computer operating system and type of web browser you are using, as well as the name of your ISP. This information is used to analyze overall trends to help us improve the Edge Up Network &amp; Mycity.com service. The linkage between your IP address and your personally identifiable information is never shared with third-parties without your permission or except when required by law.
</p> 
